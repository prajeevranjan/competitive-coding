#include <iostream>
using namespace std;

int main() {
  
    // for loop
    for(int i=0; i < 5; i++){
        cout << "hello world" << endl;
    }

    // while loop
    int k = 0;
    while(k < 5){
        cout << "hello" << endl;
        k++;
    }

    // do-while loop
    int j = 0;
    do{
        cout << "rajeev" << endl;
        j++;
    }while(j < 5);
}