#include <iostream>
using namespace std;

int main(){
    int a = 5;

    // if statement
    if (a = 5){
        cout << "a is equal to 5" << endl;
    }

    // else-if statement
    else if ( a < 5){
        cout << "a is less than 5" << endl;
    }

    // else statement 
    else{
        cout << " a is greater than 5" << endl; 
    }
}